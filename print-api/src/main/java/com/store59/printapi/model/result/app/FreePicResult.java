package com.store59.printapi.model.result.app;

public class FreePicResult {

	private byte has_opend;
	private String phone;
	private String url;

	public byte getHas_opend() {
		return has_opend;
	}

	public void setHas_opend(byte has_opend) {
		this.has_opend = has_opend;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
